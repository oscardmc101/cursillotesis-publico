DROP FUNCTION IF EXISTS public.rpc_list_grupos_cursos(uuid);
CREATE OR REPLACE FUNCTION public.rpc_list_grupos_cursos(p_id_cursillo uuid)
RETURNS TABLE(
  id_grupo_curso uuid,
  id_cursillo uuid,
  nombre text,
  descripcion text,
  orden integer,
  es_activo boolean,
  requiere_password boolean,
  total_cursos bigint,
  cursos_publicados bigint
)
LANGUAGE plpgsql
STABLE SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_is_staff boolean;
  v_is_student boolean;
BEGIN
  v_is_staff :=
    public.has_cursillo_role(p_id_cursillo, 'ADMINISTRADOR')
    OR public.has_cursillo_role(p_id_cursillo, 'DOCENTE');
  v_is_student := public.has_cursillo_role(p_id_cursillo, 'ESTUDIANTE');

  IF NOT (v_is_staff OR v_is_student) THEN
    RAISE EXCEPTION 'No autorizado para listar grupos de cursos';
  END IF;

  RETURN QUERY
  SELECT
    gc.id_grupo_curso,
    gc.id_cursillo,
    gc.nombre::text,
    gc.descripcion::text,
    gc.orden,
    gc.es_activo,
    gc.requiere_password,
    COUNT(c.id_curso)::bigint AS total_cursos,
    COUNT(c.id_curso) FILTER (WHERE c.es_publicado = true)::bigint AS cursos_publicados
  FROM public.grupos_cursos gc
  LEFT JOIN public.cursos c ON c.id_grupo_curso = gc.id_grupo_curso
  WHERE gc.id_cursillo = p_id_cursillo
    AND (
      v_is_staff
      OR (
        gc.es_activo = true
        AND EXISTS (
          SELECT 1
          FROM public.cursos c2
          WHERE c2.id_grupo_curso = gc.id_grupo_curso
            AND c2.es_publicado = true
        )
      )
    )
  GROUP BY gc.id_grupo_curso, gc.id_cursillo, gc.nombre, gc.descripcion, gc.orden, gc.es_activo, gc.requiere_password
  ORDER BY gc.orden ASC, gc.nombre ASC;
END;
$function$;

GRANT EXECUTE ON FUNCTION public.rpc_list_grupos_cursos(uuid) TO authenticated;

CREATE OR REPLACE FUNCTION public.rpc_set_grupo_curso_password(
  p_id_grupo_curso uuid,
  p_requiere_password boolean,
  p_password text DEFAULT NULL
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_id_cursillo uuid;
BEGIN
  SELECT id_cursillo
  INTO v_id_cursillo
  FROM public.grupos_cursos
  WHERE id_grupo_curso = p_id_grupo_curso;

  IF v_id_cursillo IS NULL THEN
    RAISE EXCEPTION 'Grupo de curso no encontrado';
  END IF;

  IF NOT (
    public.has_cursillo_role(v_id_cursillo, 'ADMINISTRADOR')
    OR public.has_cursillo_role(v_id_cursillo, 'DOCENTE')
  ) THEN
    RAISE EXCEPTION 'No autorizado para configurar la contrasena del grupo';
  END IF;

  IF p_requiere_password AND length(trim(coalesce(p_password, ''))) = 0 THEN
    RAISE EXCEPTION 'La contrasena del grupo es obligatoria';
  END IF;

  PERFORM set_config('app.allow_password_hash_write', 'on', true);

  UPDATE public.grupos_cursos
  SET
    requiere_password = p_requiere_password,
    password_hash = CASE
      WHEN p_requiere_password THEN crypt(p_password, gen_salt('bf'))
      ELSE NULL
    END
  WHERE id_grupo_curso = p_id_grupo_curso;
END;
$function$;

GRANT EXECUTE ON FUNCTION public.rpc_set_grupo_curso_password(uuid, boolean, text) TO authenticated;

CREATE OR REPLACE FUNCTION public.rpc_set_curso_password(
  p_id_curso uuid,
  p_requiere_password boolean,
  p_password text DEFAULT NULL
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_id_cursillo uuid;
  v_id_docente uuid;
  v_current_usuario uuid;
BEGIN
  SELECT c.id_cursillo, c.id_docente
  INTO v_id_cursillo, v_id_docente
  FROM public.cursos c
  WHERE c.id_curso = p_id_curso;

  IF v_id_cursillo IS NULL THEN
    RAISE EXCEPTION 'Curso no encontrado';
  END IF;

  v_current_usuario := public.current_id_usuario();

  IF NOT (
    public.has_cursillo_role(v_id_cursillo, 'ADMINISTRADOR')
    OR (public.has_cursillo_role(v_id_cursillo, 'DOCENTE') AND v_id_docente = v_current_usuario)
  ) THEN
    RAISE EXCEPTION 'No autorizado para configurar la contrasena del curso';
  END IF;

  IF p_requiere_password AND length(trim(coalesce(p_password, ''))) = 0 THEN
    RAISE EXCEPTION 'La contrasena del curso es obligatoria';
  END IF;

  PERFORM set_config('app.allow_password_hash_write', 'on', true);

  UPDATE public.cursos
  SET
    requiere_password = p_requiere_password,
    password_hash = CASE
      WHEN p_requiere_password THEN crypt(p_password, gen_salt('bf'))
      ELSE NULL
    END
  WHERE id_curso = p_id_curso;
END;
$function$;

GRANT EXECUTE ON FUNCTION public.rpc_set_curso_password(uuid, boolean, text) TO authenticated;

