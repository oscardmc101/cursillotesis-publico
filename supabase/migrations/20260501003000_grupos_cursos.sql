  BEFORE INSERT OR UPDATE OF password_hash ON public.cursos
  FOR EACH ROW
  EXECUTE FUNCTION public.trg_prevent_direct_password_hash_write();

-- Backfill: cada cursillo con cursos existentes recibe un grupo General.
INSERT INTO public.grupos_cursos (id_cursillo, nombre, descripcion, orden, es_activo)
SELECT DISTINCT c.id_cursillo, 'General', 'Cursos existentes antes de la organizacion por grupos.', 0, true
FROM public.cursos c
WHERE c.id_cursillo IS NOT NULL
  AND NOT EXISTS (
    SELECT 1
    FROM public.grupos_cursos gc
    WHERE gc.id_cursillo = c.id_cursillo
      AND lower(trim(gc.nombre)) = lower(trim('General'))
  );

UPDATE public.cursos c
SET id_grupo_curso = gc.id_grupo_curso
FROM public.grupos_cursos gc
WHERE c.id_grupo_curso IS NULL
  AND gc.id_cursillo = c.id_cursillo
  AND lower(trim(gc.nombre)) = lower(trim('General'));

-- Evita asignar un curso a un grupo que pertenece a otro cursillo.
CREATE OR REPLACE FUNCTION public.trg_validate_curso_grupo_cursillo()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_grupo_cursillo uuid;
BEGIN
  IF NEW.id_grupo_curso IS NULL THEN
    RETURN NEW;
  END IF;

  SELECT gc.id_cursillo
  INTO v_grupo_cursillo
  FROM public.grupos_cursos gc
  WHERE gc.id_grupo_curso = NEW.id_grupo_curso;

  IF v_grupo_cursillo IS NULL THEN
    RAISE EXCEPTION 'Grupo de curso no encontrado';
  END IF;

  IF v_grupo_cursillo <> NEW.id_cursillo THEN
    RAISE EXCEPTION 'El grupo seleccionado no pertenece al cursillo del curso';
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS validate_curso_grupo_cursillo ON public.cursos;
CREATE TRIGGER validate_curso_grupo_cursillo
  BEFORE INSERT OR UPDATE OF id_cursillo, id_grupo_curso ON public.cursos
  FOR EACH ROW
  EXECUTE FUNCTION public.trg_validate_curso_grupo_cursillo();

