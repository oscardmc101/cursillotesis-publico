CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS public.grupos_cursos (
  id_grupo_curso uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  id_cursillo uuid NOT NULL REFERENCES public.cursillos(id_cursillo) ON DELETE CASCADE,
  nombre text NOT NULL,
  descripcion text NULL,
  orden integer GENERATED BY DEFAULT AS IDENTITY,
  es_activo boolean NOT NULL DEFAULT true,
  requiere_password boolean NOT NULL DEFAULT false,
  password_hash text NULL,
  fecha_creacion timestamptz NOT NULL DEFAULT now(),
  fec_insercion timestamptz NULL,
  usu_insercion varchar NULL,
  fec_modificacion timestamptz NULL,
  usu_modificacion varchar NULL,
  CONSTRAINT grupos_cursos_nombre_not_blank CHECK (length(trim(nombre)) > 0)
);

ALTER TABLE public.grupos_cursos
  ADD COLUMN IF NOT EXISTS requiere_password boolean NOT NULL DEFAULT false,
  ADD COLUMN IF NOT EXISTS password_hash text NULL;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_constraint
    WHERE conname = 'grupos_cursos_password_consistente'
      AND conrelid = 'public.grupos_cursos'::regclass
  ) THEN
    ALTER TABLE public.grupos_cursos
      ADD CONSTRAINT grupos_cursos_password_consistente
      CHECK (
        (requiere_password = false AND password_hash IS NULL)
        OR (requiere_password = true AND password_hash IS NOT NULL)
      );
  END IF;
END $$;

CREATE UNIQUE INDEX IF NOT EXISTS idx_grupos_cursos_nombre_cursillo
  ON public.grupos_cursos (id_cursillo, lower(trim(nombre)));

CREATE INDEX IF NOT EXISTS idx_grupos_cursos_cursillo_orden
  ON public.grupos_cursos (id_cursillo, es_activo, orden, nombre);

ALTER TABLE public.cursos
  ADD COLUMN IF NOT EXISTS id_grupo_curso uuid NULL
  REFERENCES public.grupos_cursos(id_grupo_curso) ON DELETE SET NULL;

ALTER TABLE public.cursos
  ADD COLUMN IF NOT EXISTS requiere_password boolean NOT NULL DEFAULT false,
  ADD COLUMN IF NOT EXISTS password_hash text NULL;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_constraint
    WHERE conname = 'cursos_password_consistente'
      AND conrelid = 'public.cursos'::regclass
  ) THEN
    ALTER TABLE public.cursos
      ADD CONSTRAINT cursos_password_consistente
      CHECK (
        (requiere_password = false AND password_hash IS NULL)
        OR (requiere_password = true AND password_hash IS NOT NULL)
      );
  END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_cursos_cursillo_grupo
  ON public.cursos (id_cursillo, id_grupo_curso);

CREATE OR REPLACE FUNCTION public.trg_prevent_direct_password_hash_write()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  IF COALESCE(current_setting('app.allow_password_hash_write', true), '') = 'on' THEN
    RETURN NEW;
  END IF;

  IF TG_OP = 'INSERT' THEN
    IF NEW.password_hash IS NOT NULL THEN
      RAISE EXCEPTION 'password_hash solo puede configurarse mediante RPC segura';
    END IF;
  ELSIF NEW.password_hash IS DISTINCT FROM OLD.password_hash THEN
    RAISE EXCEPTION 'password_hash solo puede configurarse mediante RPC segura';
  END IF;

  RETURN NEW;
END;
$function$;

DROP TRIGGER IF EXISTS prevent_direct_grupo_password_hash_write ON public.grupos_cursos;
CREATE TRIGGER prevent_direct_grupo_password_hash_write
  BEFORE INSERT OR UPDATE OF password_hash ON public.grupos_cursos
  FOR EACH ROW
  EXECUTE FUNCTION public.trg_prevent_direct_password_hash_write();

